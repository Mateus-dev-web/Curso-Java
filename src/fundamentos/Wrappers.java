package fundamentos;

public class Wrappers {
     public static void main(String[] args) {
		 
    	 Byte b = 100;
    	 Short s = 1000;
    	 Integer i = 1000;
    	 Long l = 10000L;
    	 
    	 System.out.println(b.byteValue());
    	 System.out.println(s.toString().length());
    	 System.out.println(i);
    	 System.out.println(i* 2);
    	 System.out.println(l);
    	
	}
}
